export const getTranslation = (lang, key, params = {}) => {
    const language = translations[lang] || translations['en'];
    let text = language[key] || key;

    Object.keys(params).forEach(param => {
        text = text.replace(`{${param}}`, params[param]);
    });

    return text;
};
